// Vamos acessar um caracter de uma string usando o metodo .at(). Começamos criando a variavel q vai guardar nossa string:

const minhaString = "Olá, mundinho!";

// Agora, usando o metodo at() vamos acessar os caracteres atraves do indice:

console.log(minhaString.at(6));
console.log(minhaString.at(1));

// com o indice negativo, acessamos a lista de do final para o inicio: 

console.log(minhaString.at(-1))
console.log(minhaString.at(-3))


